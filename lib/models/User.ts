import mongoose, { Schema, model, models } from "mongoose";

const UserSchema = new Schema(
  {
    email: {
      type: String,
      required: true,
      unique: true,
      lowercase: true,
      trim: true,
    },
    password: {
      type: String,
      required: true,
    },
    name: {
      type: String,
      required: true,
    },
    image: {
      type: String,
      default: "",
    },
    role: {
      type: String,
      enum: ["employee", "cellchef", "departmentchef", "admin"],
      default: "employee",
    },
  },
  {
    timestamps: true,
  }
);

export const User = models.User || model("User", UserSchema);